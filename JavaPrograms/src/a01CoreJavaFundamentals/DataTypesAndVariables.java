package a01CoreJavaFundamentals;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		// Integer type
        int age = 25; 
        System.out.println("Age: " + age);

        // Float type
        float salary = 45000.50f; 
        System.out.println("Salary: " + salary);

        // Double type
        double pi = 3.141592653589793;
        System.out.println("Value of Pi: " + pi);

        // Character type
        char grade = 'A'; 
        System.out.println("Grade: " + grade);

        // Boolean type
        boolean isTrue = true; 
        System.out.println("Is True? " + isTrue);

        // Byte type
        byte smallNumber = 127; 
        System.out.println("Small Number (byte): " + smallNumber);

        // Short type
        short mediumNumber = 32000; 
        System.out.println("Medium Number (short): " + mediumNumber);

        // Long type
        long largeNumber = 123456789L; 
        System.out.println("Large Number (long): " + largeNumber);

        // String (not a primitive type, but commonly used)
        String message = "Hello, Java!"; 
        System.out.println("Message: " + message);
	}

}
